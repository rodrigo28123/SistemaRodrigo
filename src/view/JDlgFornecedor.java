    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        // TODO add your handling code here:
        int resp = JOptionPane.showConfirmDialog(null, "Confirma exclus√£o ?",
                "Pergunta", JOptionPane.YES_NO_OPTION );
                
        if( resp == JOptionPane.YES_OPTION){
            Fornecedor fornecedor = viewBean();
            FornecedorDAO fornecedorDAO = new FornecedorDAO();
            fornecedorDAO.delete(fornecedor);
            JOptionPane.showMessageDialog(null, "Exclus√£o efetuada");
        }else {
            JOptionPane.showMessageDialog(null, "Exclus√£o cancelada");
        }
   
        
    }//GEN-LAST:event_jBtnExcluirActionPerformed

